    private void SearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SearchMouseClicked
        // To search depending on selected item of comboBox
        try
        {
            MyConnection conn =new MyConnection();
            conn.connect();
            Statement st = conn.con.createStatement();
            
            if(searchField.getSelectedItem().equals("Librarian attendance"))
              {
                ResultSet rs = st.executeQuery("select fullname as 'Full name', clockIN as 'Clock In',clockOUT as 'Clock Out' from login inner join userInfo on login.UID = userInfo.UID inner join attendance on login.UID = attendance.UID where login.acc_ID = 2");
                information.setModel(DbUtils.resultSetToTableModel(rs));
              }
            if(searchField.getSelectedItem().equals("Books"))
              {
                ResultSet rs = st.executeQuery("select bookID as 'Book number', bookName as 'Book name', author as 'Author', edition as 'Edition', bookStatus as 'Availability' from books inner join bookCatigory where books.CatID = bookCatigory.CatID");
                information.setModel(DbUtils.resultSetToTableModel(rs));
              }
            if(searchField.getSelectedItem().equals("Members"))
              {
                ResultSet rs = st.executeQuery("select fullname as 'Full name', localAdd as 'Local address', phoneNo as 'Phone number', Email as 'Email address' from userInfo inner join login where userInfo.UID = login.UID and login.acc_ID = 3");
                information.setModel(DbUtils.resultSetToTableModel(rs));
              }
            
            st.close();
            conn.con.close();

        }

        catch (SQLException ex)
        {
            Logger.getLogger(Members.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch (ClassNotFoundException ex)
        {
            Logger.getLogger(Members.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_SearchMouseClicked

    
    
    private void SaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SaveMouseClicked
        // To save text to file
        try
        {
         // Create file:
          // a. Get the current path where this project is saved inside the computer
          String CurrentProjectPath = System.getProperty("user.dir");
          
          // b. Create a file and save it the path stored in the string variable
          File f =new File (CurrentProjectPath + "\\src\\Reports\\" + ReportName.getText() + ".txt");
          
          if(f.exists())
          {
           JOptionPane.showMessageDialog(null, "Report for this user is already exist!");  // Message box
          }
          else
          {
           PrintWriter pw = new PrintWriter(f);
           pw.println(Report1.getText());
           pw.close();
          }
        }
        
        
        catch(Exception e)
        {
          System.out.println("File output error");
        }
    }//GEN-LAST:event_SaveMouseClicked

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
